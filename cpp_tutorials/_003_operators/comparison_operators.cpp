#include<iostream>
using namespace std;

int main() {
	// Here, we learn about Comparison Operators.
	
	int iVar1 = 10, iVar2 = 20;
	cout << "10 == 20 : " << (iVar1 == iVar2) << endl;
	cout << "10 != 20 : " << (iVar1 != iVar2) << endl;
	cout << "10 < 20 : " << (iVar1 < iVar2) << endl;
	cout << "10 > 20 : " << (iVar1 > iVar2) << endl;
	cout << "10 <= 20 : " << (iVar1 <= iVar2) << endl;
	cout << "10 >= 20 : " << (iVar1 >= iVar2) << endl;
	return 0;
}
